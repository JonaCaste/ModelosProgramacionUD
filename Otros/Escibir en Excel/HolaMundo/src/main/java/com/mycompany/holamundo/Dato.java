public class Dato {
    
    private String dato, valor;

    public Dato() {
    }
    public Dato(String dato, String valor) {
        this.dato = dato;
        this.valor = valor;
    }

    public String getDato() {
        return dato;
    }
    public String getValor() {
        return valor;
    }

    public void setDato(String dato) {
        this.dato = dato;
    }
    public void setValor(String valor) {
        this.valor = valor;
    }
    
    
    
}
